#Faça um Programa que peça as 4 notas bimestrais e mostre a média.
b1 = float(input('Informe a nota do primeiro bimestre: '))
b2 = float(input('Informe a note do segundo bimestre: '))
b3 = float(input('Informe a nota do terceiro bimestre: '))
b4 = float(input('Inmforme a nota do quarto bimestre: '))
media = (b1 + b2 + b3 + b4) / 4
print(f'A sua média ficou: {media}')